export function getRouteMetadata(currentPath: string): RouteMetadata {
  const cleanPath = currentPath.toLowerCase().trim().replace(/\/$/, '') || '/';
  
  // Exact match
  const found = ROUTES_DATA.find(r => r.path === cleanPath);
  if (found) return found;

  // Handle hash-based fallbacks or path prefixing
  if (cleanPath.startsWith('/blog/')) {
    const blogMatch = ROUTES_DATA.find(r => r.path === cleanPath);
    if (blogMatch) return blogMatch;
  }

  // Fallback default (Homepage metadata)
  return ROUTES_DATA[0];
}
